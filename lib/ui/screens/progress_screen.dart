import 'package:flutter/material.dart';

class ProgressScreen extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: Color(0xFFF5F5F5),
      appBar: AppBar(
        title: Text('Progress'),
        backgroundColor: Color(0xFF7B68EE),
        foregroundColor: Colors.white,
        elevation: 0,
      ),
      body: Padding(
        padding: const EdgeInsets.all(20.0),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text(
              'Your Progress',
              style: TextStyle(
                fontSize: 24,
                fontWeight: FontWeight.bold,
                color: Colors.black87,
              ),
            ),
            SizedBox(height: 20),
            _buildProgressCard('Weekly Goal', '75%', Color(0xFF7B68EE)),
            SizedBox(height: 15),
            _buildProgressCard('Monthly Target', '60%', Colors.orange),
            SizedBox(height: 15),
            _buildProgressCard('Personal Best', '100%', Colors.green),
